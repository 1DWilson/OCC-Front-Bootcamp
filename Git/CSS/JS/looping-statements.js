//Looping Statements - Execute repetitive task / way to automate tasks

//Loops: for loop
for (let i = 1; i <=5; i++ ) {
    console.log("Count:" , i);
}


//Loops: while loop
let count = 1;
while (count <=5) {
    count++;
    console.log("While Count:" , count)
}



//Loops: Do-while loop
let counter = 1;
do {
    console.log("Do While Count:" , counter);
    counter++;
} while (counter <=5);